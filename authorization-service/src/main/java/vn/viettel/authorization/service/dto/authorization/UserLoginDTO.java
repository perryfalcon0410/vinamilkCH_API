package vn.viettel.authorization.service.dto.authorization;

import java.util.List;

public class UserLoginDTO {

    private Long id;
    private Long companyId;
    private String email;
    private String name;
    private String photo;
    private String role;
    private String token;
    private List<String> privilegesNames;
    private Long salonId;
    private String companyName;
    private String salonName;

    public Long getId() {
        return id;
    }

    public String getSalonName() {
        return salonName;
    }

    public void setSalonName(String salonName) {
        this.salonName = salonName;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getCompanyId() {
        return companyId;
    }

    public void setCompanyId(Long companyId) {
        this.companyId = companyId;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhoto() {
        return photo;
    }

    public void setPhoto(String photo) {
        this.photo = photo;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }

    public List<String> getPrivilegesNames() {
        return privilegesNames;
    }

    public void setPrivilegesNames(List<String> privilegesNames) {
        this.privilegesNames = privilegesNames;
    }

    public Long getSalonId() {
        return salonId;
    }

    public void setSalonId(Long salonId) {
        this.salonId = salonId;
    }

    public String getCompanyName() {
        return companyName;
    }

    public void setCompanyName(String companyName) {
        this.companyName = companyName;
    }
}
